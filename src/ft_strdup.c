#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*ptr;
	size_t	len;

	len = ft_strlen(s1) + 1;
	ptr = malloc(len);
	if (ptr == 0)
		return (0);
	return ((char *)ft_memcpy(ptr, s1, len));
}
